// Функция для обновления JSON только данными из td с классом "heaters"
function updateJsonFromHeaters() {
  // Обрабатываем таблицы tb1-tb6
  for (let i = 1; i <= 6; i++) {
    const tableId = `tb${i}`;
    const table = document.getElementById(tableId);
    
    if (table) {
      const heaterCells = table.querySelectorAll('td.heaters');
      const heaterData = [];
      
      // Собираем текст только из ячеек с классом "heaters"
      heaterCells.forEach(cell => {
        heaterData.push(cell.textContent.trim() || "");
      });
      
      // Заполняем массив до 6 элементов пустыми строками если нужно
      while (heaterData.length < 6) {
        heaterData.push("");
      }
      
      // Обновляем JSON - берем первые 6 значений
      if (jsonLoadData.tables[i-1] && jsonLoadData.tables[i-1][tableId]) {
        jsonLoadData.tables[i-1][tableId] = heaterData.slice(0, 6);
      }
    }
  }
  //console.log(jsonLoadData);
  //return jsonLoadData;
}

