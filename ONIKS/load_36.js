function loadData(jsonData) {

    for(let i = 1; i <= 6; i++) {
        const table = document.getElementById(`tb${i}`);
        if (!table) continue;
        
        const tableKey = `tb${i}`;
        const values = jsonData.tables[i-1]?.[tableKey];
        
        if (!values) {
            console.warn(`Данные для ${tableKey} не найдены`);
            continue;
        }
        
        const cells = table.querySelectorAll('td.heaters');
        
        cells.forEach((cell, index) => {
            if (values[index]) {
                cell.textContent = values[index];
            }
        });
    }
}



