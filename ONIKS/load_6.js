function addLongPressHeaters(delay = 500) {
  document.addEventListener('touchstart', (e) => {
    if (e.target.classList.contains('name_zone') && e.target.tagName === 'TD') {
      const td = e.target;
      const timer = setTimeout(() => {
        console.log('Долгое касание:', td.textContent);
        td.contentEditable = true;
        td.classList.add('editing'); // Подсветка ВКЛ
        td.focus();
        
        // Обработчик потери фокуса
        const onBlur = () => {
          td.contentEditable = false;
          td.classList.remove('editing'); // Подсветка ВЫКЛ
          updateJsonFromHeaters();
          saveHeatersName();
          td.removeEventListener('blur', onBlur);
        };
        td.addEventListener('blur', onBlur);
        
      }, delay);
      
      e.target._timer = timer;
      e.target._startX = e.touches[0].clientX;
      e.target._startY = e.touches[0].clientY;
    }
  }, { passive: true });

  document.addEventListener('touchend', (e) => {
    const td = e.target.closest('td.name_zone');
    if (td && td._timer) {
      clearTimeout(td._timer);
      delete td._timer;
    }
  }, { passive: true });
}

